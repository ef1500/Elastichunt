def print_banner():
  if os.name == 'nt':
    colorama.init()

  rows, columns = os.get_terminal_size()
  print(Fore.LIGHTRED_EX + Style.BRIGHT + f"{banner:^{columns}}")

  # move the cursor to the second row
  print(Fore.RESET + Style.RESET_ALL + f"\033[{rows - 1};0H")

  if os.name == 'nt':
    colorama.deinit()
